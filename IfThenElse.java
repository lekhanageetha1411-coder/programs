class ifthenelse{
    public static void main(String[] args){
        int testScore=50;

        if(testScore>=90){
            System.out.println("A");
        }
        else if(testScore>=80){
            System.out.println("B");
        }
        else if(testScore>=70){
            System.out.println("C");
        }
        else if(testScore>=60){
            System.out.println("D");
        }
        else{
            System.out.println("F");
        }
    }
}
